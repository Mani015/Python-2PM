# Extend method():Add the elements of a list (or any iterable), to the end of the current list

laptops = ['dell','hp','lenovo','acer','realme','mac']
print(laptops)
# ['dell', 'hp', 'lenovo', 'acer', 'realme', 'mac']

# adding two values
laptops.extend(['acess','samsung'])
# print(laptops)
# ['dell', 'hp', 'lenovo', 'acer', 'realme', 'mac', 'acess', 'samsung']



laptops.extend(['mi','sony','philip','micromax'])
print(laptops)
# ['dell', 'hp', 'lenovo', 'acer', 'realme', 'mac', 'acess', 'samsung', 'mi', 'sony', 'philip', 'micromax']
